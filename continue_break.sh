#------------------------------------------------------Break------------------------------------------------------------------#
for (( i=1; i<=10; i++ ))
do
    if [ $i -gt 5 ]
      then 
      break
    fi
echo $i
done

#------------------------------------------------------Continue------------------------------------------------------------------#

for (( i=1; i<=10; i++ ))
do
    if [ $i -eq 5 -o $i -eq 3 ]
      then 
      continue
    fi
echo $i
done